#!/bin/bash

#wysietlenie zawartosci zmienny za pomoca komendy echo
#aby wyswietlic zmienna nalezy umiescic ja w {} poprzedzajac $

#zmienna PATH - domyslna sciezka dostepu
echo ${PATH}

#zmienna RANDOM - generator losowych liczb
echo ${RANDOM}

#zmienna PWD - sciezka aktualnego katalogu roboczego
echo ${PWD}

#zmienna PS1 - ustawiajaca znak zachety
echo ${PS1}

#zmienna USER - zwraca nazwe uzytkownika
echo ${USER}

#zmienna HOSTNAME - nazwa serwera
echo ${HOSTNAME}

#zmienna OSTYPE - typ systemu
echo ${OSTYPE}
